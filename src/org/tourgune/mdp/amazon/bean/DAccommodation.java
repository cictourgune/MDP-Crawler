package org.tourgune.mdp.amazon.bean;

public class DAccommodation {

	private int id_accommodation;
	private String name;
	private String address;
	private String zip_code;
	private String locality;
	private int id_geography;
	private int category;
	private double latitude;
	private double longitude;
	private int id_accommodation_type;
	private int id_accommodation_chain;
	
	public int getId_accommodation() {
		return id_accommodation;
	}
	public void setId_accommodation(int id_accommodation) {
		this.id_accommodation = id_accommodation;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getZip_code() {
		return zip_code;
	}
	public void setZip_code(String zip_code) {
		this.zip_code = zip_code;
	}
	public String getLocality() {
		return locality;
	}
	public void setLocality(String locality) {
		this.locality = locality;
	}
	public int getId_geography() {
		return id_geography;
	}
	public void setId_geography(int id_geography) {
		this.id_geography = id_geography;
	}
	public int getCategory() {
		return category;
	}
	public void setCategory(int category) {
		this.category = category;
	}
	public double getLatitude() {
		return latitude;
	}
	public void setLatitude(double latitude) {
		this.latitude = latitude;
	}
	public double getLongitude() {
		return longitude;
	}
	public void setLongitude(double longitude) {
		this.longitude = longitude;
	}
	public int getId_accommodation_type() {
		return id_accommodation_type;
	}
	public void setId_accommodation_type(int id_accommodation_type) {
		this.id_accommodation_type = id_accommodation_type;
	}
	public int getId_accommodation_chain() {
		return id_accommodation_chain;
	}
	public void setId_accommodation_chain(int id_accommodation_chain) {
		this.id_accommodation_chain = id_accommodation_chain;
	}
	
	
}
